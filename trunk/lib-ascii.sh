#------------------------------------------------------------------------------
# Arguments:
#   * Decimal representation of the character
function chr() {
  printf \\x$((${1}/16))$((${1}%16))
}
#------------------------------------------------------------------------------

#------------------------------------------------------------------------------
# Arguments:
#   * Character
function ord() {
  printf '%d' "'${1}"
}
